// La asignación implicita es como hemos declarado nuestras variables y constantes hasta ahora:
var numero = 15
var nombre = "Luis"

// La asignación implicita no es nada especial, es solo nuestra variable y su valor de cualquier tipo. En cambio la explicita, es especificar que tipo de dato se espera:
var nombre1:String = "Luis"
var numero1:Int = 13
// Este 'metodo' es lo similar a la especificidad en Python, pero en python se usaba la funcion a la que se deseaba convertir (int(), float(), str(), etc). La sintaxis en Swift seria, el nombre de la variable o constante y seguido 2 puntos los cuales indican que especificaremos el tipo de dato y entonces indicamos que tipo de dato se espera. Y como ultimo se le asigna el valor.
