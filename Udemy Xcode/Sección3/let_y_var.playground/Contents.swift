// Dentro de las palabras reservadas de Swift pudimos ver que se encuentran 'let' y 'var'. La palabra 'let' es para definir una constante y la palabra 'var' es para definir una variable.
// Como sus nombres lo dicen, las constantes son aquellos que sus valores no se podran cambiar ni modificar y las variables si. Su sintaxis seria:
let nombre = "Luis"
var color = "Rojo"

// Si quisieramos hacer cambios en la variable, solo se necesitaria llamar el nombre de la variable, sin la palabra reservada:
color = "Azul"
print(color)
